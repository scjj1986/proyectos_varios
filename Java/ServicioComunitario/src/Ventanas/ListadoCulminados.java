    private void txtValorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyPressed
        tblProyecto.removeAll();

        modelo = new DefaultTableModel();

        this.tblProyecto.setModel(modelo);

        modelo.addColumn("NOMBRE");
        modelo.addColumn("CUO");
        modelo.addColumn("CARRERA");
        modelo.addColumn("TUTOR ACADÉMICO");

        if (cmbBuscar.getSelectedItem().toString().equals("Nombre")){

            consulta="SELECT * FROM proyecto WHERE estado<>'ESPERA' AND (nombre='"+txtValor.getText().toUpperCase()+"' OR nombre LIKE '%"+txtValor.getText().toUpperCase()+"%' OR nombre LIKE '"+txtValor.getText().toUpperCase()+"%' OR nombre LIKE '%"+txtValor.getText().toUpperCase()+"') ORDER BY ncuo DESC";

        }
        else if (cmbBuscar.getSelectedItem().toString().equals("CUO")){

            consulta="SELECT * FROM proyecto WHERE estado<>'ESPERA' AND (ncuo='"+txtValor.getText()+"' OR ncuo LIKE '%"+txtValor.getText()+"%' OR ncuo LIKE '"+txtValor.getText()+"%' OR ncuo LIKE '%"+txtValor.getText()+"') ORDER BY ncuo DESC";

        }
        else{

            consulta="SELECT * FROM proyecto WHERE estado<>'ESPERA' AND(carrera='"+txtValor.getText().toUpperCase()+"' OR carrera LIKE '%"+txtValor.getText().toUpperCase()+"%' OR carrera LIKE '"+txtValor.getText().toUpperCase()+"%' OR carrera LIKE '%"+txtValor.getText().toUpperCase()+"') ORDER BY ncuo DESC";

        }

        bd= new BaseDatos();

        if (bd.n_tuplas(consulta)>0){

            tuplas= new String [bd.n_tuplas(consulta)][7];
            bd.obtener_tuplas(consulta,tuplas,7);
            for (int i=0; i<bd.n_tuplas(consulta); i++){

                linea= new String [4];
                linea[0]= tuplas[i][2];
                linea[1]= tuplas[i][1];
                linea[2]= tuplas[i][6];
                consulta2= "SElECT * FROM tutor WHERE ve='"+tuplas[i][3].substring(0,2)+"'AND cedula='"+tuplas[i][3].substring(2,tuplas[i][3].length())+"'";
                tuplas2= new String [bd.n_tuplas(consulta2)][7];
                bd.obtener_tuplas(consulta2,tuplas2,7);
                linea[3]= tuplas2[0][3]+" "+tuplas2[0][4];
                modelo.addRow(linea);

            }
        }
    }//GEN-LAST:event_txtValorKeyPressed

    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        int fila=tblProyecto.getSelectedRow();
        if (fila<0){

            Mensaje.showMessageDialog(this,"No se ha elegido ningún proyecto.","Error",Mensaje.ERROR_MESSAGE);

        }
        else{
            DetallesCulminados detc= new DetallesCulminados(modelo.getValueAt(fila, 0).toString(),modelo.getValueAt(fila, 1).toString(),modelo.getValueAt(fila, 2).toString(),modelo.getValueAt(fila, 3).toString());
            detc.setVisible(true);
            dispose();

        }
    }//GEN-LAST:event_btnDetallesActionPerformed

