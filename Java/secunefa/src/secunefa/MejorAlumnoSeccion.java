    private void ComboMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboMateriaActionPerformed

        if (ComboMateria.getSelectedIndex()!=-1){
            try{

                String carr=ComboCarrera.getSelectedItem().toString();
                String mater=ComboMateria.getSelectedItem().toString();
                ComboSeccion.removeAllItems();
                ComboSeccion.addItem("Seleccione...");
                lista_seccion ls= new lista_seccion();
                if (ls.existearchivo("seccion.obj"))
                {

                    ObjectInputStream entrada2=new ObjectInputStream(new FileInputStream("seccion.obj"));
                    seccion aux2=(seccion) entrada2.readObject();
                    entrada2.close();
                    ObjectInputStream entrada=new ObjectInputStream(new FileInputStream("materia.obj"));
                    materia aux=(materia) entrada.readObject();
                    entrada.close();
                    int i=1;
                    while (aux!=null)
                    {

                        if ((carr.equals(aux.carrera))  && (aux.nombre.equals(mater)) )
                        {

                            codigomateria=aux.codigo;

                        }
                        i++;
                        aux=aux.sig;
                    }
                    while (aux2!=null)
                    {

                        if (aux2.codigomateria.equals(codigomateria))
                        {

                            ComboSeccion.addItem(aux2.numero);
                        }
                        aux2=aux2.sig;
                    }
                }

            }
            catch (IOException ex) {
                System.out.println(ex);
            }catch (ClassNotFoundException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_ComboMateriaActionPerformed

