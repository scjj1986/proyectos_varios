    private void BotonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConsultarActionPerformed
        if ((ComboCarrera.getSelectedIndex()<=0) ||(ComboMateria.getSelectedIndex()<=0) || (ComboSeccion.getSelectedIndex()<=0)){

            Mensaje.showMessageDialog(this,"Verifique las selecciones","Error",Mensaje.ERROR_MESSAGE);
        }
        else{

            int mayornota=0;
            lista_inscritos li= new lista_inscritos();
            try{

                if (li.existearchivo("inscritos.obj"))
                {
                    modelo= new DefaultTableModel();
                    modelo.addColumn("Cedula");
                    modelo.addColumn("Nombre");
                    modelo.addColumn("Apellido");
                    modelo.addColumn("Direccion");
                    modelo.addColumn("Nota");
                    this.TablaAlumnos.setModel(modelo);
                    inscribe aux;
                    alumno aux2;
                    ObjectInputStream entrada,entrada2,entrada3;
                    entrada=new ObjectInputStream(new FileInputStream("inscritos.obj"));
                    aux=(inscribe) entrada.readObject();
                    entrada.close();
                    String []datos= new String[5];
                    while (aux!=null)
                    {
                        entrada2=new ObjectInputStream(new FileInputStream("alumno.obj"));
                        aux2=(alumno) entrada2.readObject();
                        entrada2.close();
                        while (aux2!=null)
                        {

                            if ((aux.codigomateria.equals(codigomateria))&& (aux.seccion.equals(ComboSeccion.getSelectedItem().toString())) && (aux2.cedula.equals(aux.cedulaalumno)) && (aux.nota>mayornota))
                            {
                                datos[0]=aux2.cedula;
                                datos[1]=aux2.nombre;
                                datos[2]=aux2.apellido;
                                datos[3]=aux2.direccion;
                                datos[4]=Integer.toString(aux.nota);
                                mayornota=aux.nota;

                            }

                            aux2=aux2.sig;

                        }

                        aux=aux.sig;
                    }
                    if (mayornota>0)
                    {
                        modelo.addRow(datos);
                    
                    }
                }
            }
            catch (IOException ex) {
                System.out.println(ex);
            }catch (ClassNotFoundException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_BotonConsultarActionPerformed

