    private void ComboCarreraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboCarreraActionPerformed
        try{
            ComboMateria.removeAllItems();
            ComboMateria.addItem("Seleccione...");
            ComboSeccion.removeAllItems();
            ComboSeccion.addItem("Seleccione...");
            lista_materia lm= new lista_materia();
            if (lm.existearchivo("materia.obj"))
            {

                ObjectInputStream entrada=new ObjectInputStream(new FileInputStream("materia.obj"));
                materia aux=(materia) entrada.readObject();
                entrada.close();
                while (aux!=null)
                {

                    if (aux.carrera.equals(ComboCarrera.getSelectedItem().toString()))
                    {

                        ComboMateria.addItem(aux.nombre);
                    }

                    aux=aux.sig;
                }

            }

        }
        catch (IOException ex) {
            System.out.println(ex);
        }catch (ClassNotFoundException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_ComboCarreraActionPerformed

