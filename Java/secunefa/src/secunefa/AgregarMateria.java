    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        char c= evt.getKeyChar();
        if ((c<'0') || (c>'9')) evt.consume();
    }//GEN-LAST:event_txtCodigoKeyTyped

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char c= evt.getKeyChar();
        if (((c<'a') || (c>'z')) && ((c<'A') || (c>'Z')) && (c!=' ')) evt.consume();
    }//GEN-LAST:event_txtNombreKeyTyped

    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        lista_materia lm=new lista_materia();
        try{
            
            if ((txtCodigo.getText().equals("")) || (txtNombre.getText().equals("")))
            {
                Mensaje.showMessageDialog(this,"No puede dejar campos vacÃ­os","Error",Mensaje.ERROR_MESSAGE);
            }
            else if(lm.codigorepetido(txtCodigo.getText()))
            {
                Mensaje.showMessageDialog(this,"El cÃ³digo "+txtCodigo.getText()+" ya fue ingresado en la base de datos. Intente de nuevo.","CÃ©dula Repetida",Mensaje.ERROR_MESSAGE);
            
            
            }
            else if(lm.nombrerepetido(txtNombre.getText().toUpperCase(),nombrecarrera))
            {
                Mensaje.showMessageDialog(this,"El nombre "+txtNombre.getText()+" ya fue ingresado en la base de datos. Intente de nuevo.","CÃ©dula Repetida",Mensaje.ERROR_MESSAGE);
            
            
            }
            else
            {
                materia mat= new materia (txtCodigo.getText(),txtNombre.getText().toUpperCase(),nombrecarrera);
                lm.guardarmateria(mat);
                Mensaje.showMessageDialog(null,"Datos almacenados exitosamente.","Info",Mensaje.INFORMATION_MESSAGE);
                txtNombre.setText("");
                txtCodigo.setText("");
            }
        
        }catch (IOException ex) {
            System.out.println(ex);
            }catch (ClassNotFoundException ex) {
               System.out.println(ex);
            }
        
    }//GEN-LAST:event_BotonGuardarActionPerformed

