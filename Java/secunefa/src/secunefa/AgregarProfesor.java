    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        
        lista_profesor lp=new lista_profesor();
        try{
            
            if ((txtNombre.getText().equals("")) || (txtCedula.getText().equals("")) || (txtApellido.getText().equals("")) || (txtDireccion.getText().equals("")) || (txtTelefono.getText().equals("")))
            {
                Mensaje.showMessageDialog(this,"No puede dejar campos vacíos","Error",Mensaje.ERROR_MESSAGE);
            }
            else if(lp.cedularepetida(txtCedula.getText()))
            {
                Mensaje.showMessageDialog(this,"La cedula "+txtCedula.getText()+" ya fue ingresada en la base de datos. Intente de nuevo.","Cédula Repetida",Mensaje.ERROR_MESSAGE);
            
            
            }
            else
            {
                profesor prof= new profesor (txtCedula.getText(),txtNombre.getText().toUpperCase(),txtApellido.getText().toUpperCase(),txtDireccion.getText().toUpperCase(),txtTelefono.getText());
                lp.guardarprofesor(prof);
                Mensaje.showMessageDialog(null,"Datos almacenados exitosamente.","Info",Mensaje.INFORMATION_MESSAGE);
                txtCedula.setText("");
                txtNombre.setText("");
                txtApellido.setText("");
                txtDireccion.setText("");
                txtTelefono.setText("");
            }
        
        }catch (IOException ex) {
            System.out.println(ex);
            }catch (ClassNotFoundException ex) {
               System.out.println(ex);
            }
        
        
    }//GEN-LAST:event_BotonGuardarActionPerformed

    private void txtCedulaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCedulaKeyTyped
        char c= evt.getKeyChar();
        if ((c<'0') || (c>'9')) evt.consume();
    }//GEN-LAST:event_txtCedulaKeyTyped

    private void txtApellidoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtApellidoKeyTyped
        char c= evt.getKeyChar();
        if (((c<'a') || (c>'z')) && ((c<'A') || (c>'Z')) && (c!=' ')) evt.consume();
    }//GEN-LAST:event_txtApellidoKeyTyped

