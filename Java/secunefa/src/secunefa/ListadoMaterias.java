    private void BotonAgregarSeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarSeccionActionPerformed
        int fila=TablaMaterias.getSelectedRow();
        if (fila<0)
        {
            Mensaje.showMessageDialog(this,"No ha seleccionado la materia. Intente de nuevo.","Error",Mensaje.ERROR_MESSAGE);
        
        }
        else{
            String codigo= modelo.getValueAt(fila, 0).toString();
            String asign= modelo.getValueAt(fila, 1).toString();
            ListadoSecciones ls= new ListadoSecciones(nombrecarrera,asign,codigo);
            ls.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_BotonAgregarSeccionActionPerformed

