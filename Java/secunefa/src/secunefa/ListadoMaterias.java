    public ListadoMaterias(String carrera) {
        initComponents();
        setSize(624,424);
        nombrecarrera=carrera;
        String texto;
        texto=""
                + "*Carrera: "+carrera;
        EtqListadoCarrera.setText(texto);
        setResizable(false);
        lista_materia lm= new lista_materia();
        try{
        
            if (lm.existearchivo("materia.obj"))
            {
                modelo= new DefaultTableModel();
                modelo.addColumn("CÃ³digo");
                modelo.addColumn("Nombre");
                modelo.addColumn("Secciones");
                this.TablaMaterias.setModel(modelo);
                materia aux;
                ObjectInputStream entrada=new ObjectInputStream(new FileInputStream("materia.obj"));
                aux=(materia) entrada.readObject();
                entrada.close();
                String []datos= new String[3];
                while (aux!=null)
                {
                    if (aux.carrera.equals(nombrecarrera))
                    {
                        datos[0]=aux.codigo;
                        datos[1]=aux.nombre;
                        datos[2]=Integer.toString(aux.secciones);
                        modelo.addRow(datos);
                    
                    }
                    aux=aux.sig;
                }
            }
        }
        catch (IOException ex) {
        System.out.println(ex);
        }catch (ClassNotFoundException ex) {
               System.out.println(ex);
        }
    }

