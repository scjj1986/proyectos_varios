    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        
        int item=ComboNota.getSelectedIndex();
        try{
            
            if (item==0)
            {
                Mensaje.showMessageDialog(this,"Elija la nota","Error",Mensaje.ERROR_MESSAGE);
            }
            
            else
            {
                
                Mensaje.showMessageDialog(null,"Datos almacenados exitosamente.","Info",Mensaje.INFORMATION_MESSAGE);
                lista_inscritos li=new lista_inscritos();
                li.asignanota(codigo,cedulaalumno,ComboNota.getSelectedItem().toString());
                ListadoAlumnos la= new ListadoAlumnos (carrera,asignatura,codigo,seccion);
                la.setVisible(true);
                dispose();
            }
        
        }catch (IOException ex) {
            System.out.println(ex);
            }catch (ClassNotFoundException ex) {
               System.out.println(ex);
            }
    }//GEN-LAST:event_BotonGuardarActionPerformed

