    public AsignarNota(String carr,String asign,String cod,String sec,String ced) {
        initComponents();
        carrera=carr;
        asignatura=asign;
        codigo=cod;
        seccion=sec;
        cedulaalumno=ced;
        String texto,texto2,texto3,texto4;
        texto="*Carrera: "+carrera;
        texto2=" Materia: "+asign;
        texto3=" Código: "+cod;
        texto4=" Sección: "+sec;
        EtqCarrera.setText(texto);
        EtqMateria.setText(texto2);
        EtqCodigo.setText(texto3);
        EtqSeccion.setText(texto4);
        try{
            ObjectInputStream entrada=new ObjectInputStream(new FileInputStream("alumno.obj"));
            alumno aux=(alumno)entrada.readObject();
            entrada.close();
            while (aux!=null)
            {
                if(ced.equals(aux.cedula))
                {
                    texto="*Cédula: "+aux.cedula;
                    texto2=" Nombre: "+aux.nombre;
                    texto3=" Apellido: "+aux.apellido;
                    texto4=" Direccion: "+aux.direccion;
                    EtqCedula.setText(texto);
                    EtqNombre.setText(texto2);
                    EtqApellido.setText(texto3);
                    EtqDireccion.setText(texto4);
                }
                aux=aux.sig;
            }
        }
        catch (IOException ex) {
        System.out.println(ex);
        }catch (ClassNotFoundException ex) {
               System.out.println(ex);
        }
        setSize(624,576);
        setResizable(false);
    }

