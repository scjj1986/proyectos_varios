    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        
        try{
            lista_carrera lc=new lista_carrera();
            if (txtNombre.getText().equals(""))
            {
                Mensaje.showMessageDialog(this,"No puede dejar campos vac√≠os","Error",Mensaje.ERROR_MESSAGE);
            }
            else if(lc.nombrerepetido(txtNombre.getText().toUpperCase())){
            
                 Mensaje.showMessageDialog(this,"La carrera "+txtNombre.getText().toUpperCase()+" ya fue ingresada en la base de datos. Intente con otro nombre.","Nombre Repetido",Mensaje.ERROR_MESSAGE);
            
            
            }
            else
            {
                carrera car= new carrera (txtNombre.getText().toUpperCase());
                lc.guardarcarrera(car);
                Mensaje.showMessageDialog(null,"Datos almacenados exitosamente.","Info",Mensaje.INFORMATION_MESSAGE);
               txtNombre.setText(""); 
            }
        
        
        }catch (IOException ex) {
            System.out.println(ex);
            }catch (ClassNotFoundException ex) {
               System.out.println(ex);
            }
        
        
        
    }//GEN-LAST:event_BotonGuardarActionPerformed

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char c= evt.getKeyChar();
        if (((c<'a') || (c>'z')) && ((c<'A') || (c>'Z')) && (c!=' ')) evt.consume();
    }//GEN-LAST:event_txtNombreKeyTyped

