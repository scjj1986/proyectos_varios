        public boolean esDecimal(String cad)
        {
            try
            {
              Double.parseDouble(cad);
              return true;
            }
            catch(NumberFormatException nfe)
            {
              return false;
            }
        }
    
    public String obtenerfecha(){
        
        
        String valor="";
        try{
            
            Date fecha= new Date();
            fecha = dchFechaFac.getDate();
            SimpleDateFormat fch = new SimpleDateFormat(dchFechaFac.getDateFormatString());
            valor=String.valueOf(fch.format(fecha));
        
        }
        catch (NullPointerException ex){
        valor="";
        return valor;
        }
        return valor;
    }
    
    private void txtRIFFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtRIFFocusLost
        consulta="SELECT * FROM proveedor WHERE rif='"+cmbRIF.getSelectedItem().toString()+txtRIF.getText()+"'";
        bd= new BaseDatos();
        if (bd.n_tuplas(consulta)>0){

            tuplas= new String[bd.n_tuplas(consulta)][7];
            bd.obtener_tuplas(consulta, tuplas, 7);
            txtNombreProv.setText(tuplas[0][5]);
            txtDirProv.setText(tuplas[0][3]);
            txtEmailProv.setText(tuplas[0][4]);
            txtEmailProv.setText(tuplas[0][4]);
            cmbTlfProv.removeAllItems();
            String mtlf=tuplas[0][2].substring(0, 4);
            String tlf=tuplas[0][2].substring(2,tuplas[0][2].length());
            cmbTlfProv.addItem(mtlf);
            cmbTlfProv.addItem("0295");
            cmbTlfProv.addItem("0416");
            cmbTlfProv.addItem("0426");
            cmbTlfProv.addItem("0414");
            cmbTlfProv.addItem("0424");
            cmbTlfProv.addItem("0412");
            txtTlfProv.setText(tlf);
            cmbCedResp.removeAllItems();
            String mced=tuplas[0][6].substring(0, 2);
            String ced=tuplas[0][6].substring(2,tuplas[0][6].length());
            cmbCedResp.addItem(mced);
            cmbCedResp.addItem("V-");
            cmbCedResp.addItem("E-");
            txtCedResp.setText(ced);

        }
    }//GEN-LAST:event_txtRIFFocusLost

